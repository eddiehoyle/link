#!/usr/bin/env python

def add_3f(array0, array1):
    return [array0[0] + array1[0],
            array0[1] + array1[1],
            array0[2] + array1[2]]

def average_3f(array0, array1):
    return [(array0[0] + array1[0]) / 2,
            (array0[1] + array1[1]) / 2,
            (array0[2] + array1[2]) / 2]
